#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int i;

	i = 0;
	while (s != NULL && *s != '\0' && f != NULL)
	{
		(*f)(i, s);
		s++;
		i++;
	}
}
