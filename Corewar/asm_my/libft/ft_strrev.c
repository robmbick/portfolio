#include "libft.h"

char	*ft_strrev(char *str)
{
	int		n;
	int		i;
	char	a;

	i = 0;
	n = 0;
	while (str[n] != '\0')
		n++;
	n--;
	while (i < n)
	{
		a = str[n];
		str[n] = str[i];
		str[i] = a;
		i++;
		n--;
	}
	return (str);
}
