#include "libft.h"

char	*ft_swap(char *s, int c1, int c2)
{
	char	tmp;

	if (s)
	{
		tmp = s[c1];
		s[c1] = s[c2];
		s[c2] = tmp;
		return ((char *)s);
	}
	return (NULL);
}
